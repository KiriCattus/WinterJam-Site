<Meta title="FAQ" />
<h1>WinterJam 2024 FAQ</h1>

<ol
	class="text-justify md:child:text-xl child:text-lg child:rounded-lg child:bg-slate-950 child:bg-opacity-30 child:p-4"
>
	<li>
		<h4>Can I submit multiple mods to this event?</h4>
		<span>
			Yes, you may create and submit multiple submissions to this event.<br />
			If you plan on doing so, please take care of yourself and take steps to avoid stress and burnout.
		</span>
	</li>
	<li>
		<h4>Can I team up with a friend?</h4>
		<span>
			Teams are allowed! There are no hard limits to the size of a team, however we suggest maximum
			teams of roughly 4 or 5.
		</span>
	</li>
	<li>
		<h4>Can I join multiple teams?</h4>
		<span>
			Yes, you may join and contribute to multiple teams. If want to do this, you should be
			transparent with everyone in your team about other teams you are contributing to.
		</span>
	</li>
	<li>
		<h4>Can I submit the same mod on multiple modloaders?</h4>
		<span>
			While NeoForge is the primary modloader for this event, others are allowed. However, only mods
			compatible with NeoForge will be added to the pack.<br />
			Be advised that supporting multiple loaders is completely optional. Doing so will require additional
			effort and may be stressful.<br />
			If you're still interested in doing so, you can use <ExternalLink href="https://github.com/architectury/architectury-templates" class="underline">Architectury</ExternalLink>
			or <ExternalLink href="https://github.com/jaredlll08/MultiLoader-Template" class="underline">Jared's MultiLoader Template</ExternalLink> to help with this.
		</span>
	</li>
	<li>
		<h4>I am not a mod author, how can I participate?</h4>
		<span>
			Everyone is welcome to participate even if you're not a mod author!<br />
			One of the easiest ways to join in is by giving participants feedback and encouragement or play
			with the mods on the public Minecraft server.<br />
			If you're looking to get more involved you may be able to help with translating submissions, helping
			with textures/models, or contributing in other ways.<br />
			You can also use this opportunity to learn more about mod development and try to make your own
			mods in the future!
		</span>
	</li>
	<li>
		<h4>Can I submit a mod from previous WinterJam events?</h4>
		<span>
			Yes, previous year entries that have been updated can be resubmitted.<br />
			These entries will not receive the same level of promotion that new submissions receive, however
			they may be included on our modpack and server.
		</span>
	</li>
	<li>
		<h4>Do Pull Requests count as being a team member?</h4>
		<span>
			Creating a Pull Request does not inherently make you a member of that team.<br />
			The team list is ultimately up to that team as a whole.
		</span>
	</li>
	<li>
		<h4>Can I submit a mod that I did not create?</h4>
		<span>
			No. Only the authors of a project may submit it to WinterJam, however you can request that the
			events team add a previous entry if it is up to date and supported by the pack and server.
		</span>
	</li>
	<li>
		<h4>Do I need to submit my project?</h4>
		<span>
			Yes you must submit your project before the event deadline via the submissions form.<br />
			(Your submission will only be counted if you are a member of the <ExternalLink href="/discord" class="underline">Discord community</ExternalLink>
			where the event is hosted)<br />
			<a class="font-bold underline hover:text-important focus:text-important" href="/jams/2024/submit">Click here to submit your project!</a>
		</span>
	</li>
	<li>
		<h4>How should teams submit their project?</h4>
		<span>
			Teams should select one team member to submit their project.<br />
			Submitting the same project multiple times is not required for updates.
		</span>
	</li>
	<li>
		<h4>Can I ask for an unrelated mod be added to the pack such as HWYLA?</h4>
		<span>
			You can request the events team add other mods to the pack during or after creation, though
			they have the final say if it fits and doesn't overshadow/distract from the creations
			submitted for this jam.
		</span>
	</li>
</ol>
